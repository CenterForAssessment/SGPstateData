Report_Text_ENGLISH <-
"The annual WIDA ACCESS test measures how well an English learner (EL) understands and uses academic English, as required by federal
law and the State ESSA Plan. When a student's English proficiency reaches the level set by the state or local school system, parents and
educators can be confident that the student can take part in school lessons and tests in English without needing extra language help. This
proficiency level is the minimum requirement for reclassifying an EL student. Parents and educators can track students' progress in academic
English by comparing their current WIDA ACCESS scores to their previous scores. This report includes two years of proficiency levels and
additional growth information based on the scores.

Proficiency: The WIDA ACCESS Individual Student Report (ISR) shows students' test results, including scale scores and proficiency levels
for each language domain and four composite areas: Oral language, Literacy, Comprehension, and Overall. The Overall Composite scale
score combines scores from Listening, Speaking, Reading, and Writing. Scale scores fall into six proficiency levels: 1-Entering, 2-Emerging,
3 -Developing, 4-Expanding, 5-Bridging, and 6-Reaching.

Growth: Georgia's Student Growth Model for English Language Proficiency (GSGM for ELP) compares the growth in English proficiency of
each EL student over one year to other EL students in the same grade across the state with similar prior year proficiency levels. This model
shows how an EL student's growth in English compares to other students in the same grade with similar proficiency levels from the previous
year. The term \"student growth percentile\" (SGP) describes this comparison, ranging from 1 (lowest growth) to 99 (highest growth).
Percentiles are not percent correct scores or proficiency levels. Even students with low English proficiency can have high growth percentiles
if their proficiency has improved significantly compared to their peers from the previous year's test.

Parents and Families: This growth report will help you understand your child's academic English language development and discuss related
questions with your child's teachers.

      *  How long will it take for my child to become proficient in English?
      *  How much growth does my child need to reach the English proficiency level for reclassification?
      *  What can I do at home to help my child learn more English?

To learn more about student academic English proficiency growth in Georgia, visit https://bit.ly/GSGM4ELP."

