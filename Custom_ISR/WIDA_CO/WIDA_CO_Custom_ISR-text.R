"The purpose of the ACCESS for ELLs\u{AE} assessment is to measure student progress toward English Language Proficiency on a yearly basis.  The
assessment serves as an indicator of when English Learners (ELs) reach language proficiency levels comparable to that of non-EL students.
The report addresses your child's English proficiency level, how much your student progressed in their development of English and if they
are on track to be proficient in English.

Achievement: The ACCESS for ELLs assessment provides a composite (overall) score and proficiency level.  The overall score consists of
listening, speaking, reading, and writing components.  The performance is described in terms of six proficiency levels. From lowest to
highest performance, the levels are: entering, emerging, developing, expanding, bridging, and reaching.  The overall score and proficiency
level are presented for your student in this report.

Growth:  The Colorado Growth Model measures the progress each student makes in a year.  The model tells us how an EL student's progress
compares to other students with similar previous ACCESS for ELLs scores.  These student growth percentile scores range from 1 (lowest growth)
to 99 (highest growth).  Percentiles are not percent correct scores, and do not reflect students' achievement levels.  Even students with
low test scores can get high student growth percentiles, if they make substantial progress since the previous year's assessment, compared
to their peers.

We hope that this report will help you understand your child's language development. This academic growth report can form the basis of
conversations with your child's school and teachers, to discuss questions such as:

      *  What steps can we take since my son's growth wasn't good enough to reach English Language Proficiency?
      *  Is my daughter's academic growth enough to move her to \"bridging\" level next year?
      *  What will it take for my son to move up to the \"reaching\" level next year?

For more information about the Colorado Growth Model visit: www.schoolview.org."


